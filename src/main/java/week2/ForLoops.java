package week2;

public class ForLoops {
    public static void main(String[] args) {

        // print numbers = 1-5
        for (int x=1; x <=5; x++){
            System.out.println(x+1);
        }

        // Print 10, 8 ...0
       for (int x=10; x>=0; x--){
           System.out.println(x + "....");
       }

       System.out.println("Rocket launch!!");
    }
}
